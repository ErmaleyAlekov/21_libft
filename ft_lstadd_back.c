#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*poslednyy;

	if (!*alst)
	{
		*alst = new;
		return ;
	}
	poslednyy = ft_lstlast(*alst);
	poslednyy->next = new;
}
