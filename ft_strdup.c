#include <stdlib.h>
#include <string.h>
#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		len;

	len = 0;
	while (src[len])
		len++;
	dest = (char *)malloc(sizeof(char) * (len + 1));
	if (dest == 0)
		return (0);
	dest[len + 1] = '\0';
	while (len > 0)
	{
		dest[len] = src[len];
		len--;
	}
	dest[0] = src[0];
	return (&dest[len]);
}
