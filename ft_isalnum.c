#include "libft.h"

int	ft_isalnum(int c)
{
	int	ale;
	int	ale2;

	ale = ft_isalpha(c);
	ale2 = ft_isdigit(c);
	if (ale == 1 || ale2 == 1)
		return (1);
	return (0);
}
